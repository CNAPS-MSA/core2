import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { ITopListBooks } from '@/shared/model/bookCatalog/top-list-books.model';

const baseApiUrl = 'services/bookcatalog/api/top-list-books';

export default class TopListBooksService {
  public find(id: string): Promise<ITopListBooks> {
    return new Promise<ITopListBooks>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: string): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ITopListBooks): Promise<ITopListBooks> {
    return new Promise<ITopListBooks>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: ITopListBooks): Promise<ITopListBooks> {
    return new Promise<ITopListBooks>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
